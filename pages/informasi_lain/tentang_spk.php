        <h5 class="mb-3">Tentang Sistem Penunjang Keputusan (SPK)</h5>

        <div class="text-justify font-weight-light"> 

            <p>
                <b>Sistem Penunjang Keputusan (SPK)</b> merupakan sebuah sistem yang digunakan untuk membantu pengambilan keputusan dalam menentukan siswa berprestasi di sekolah, khususnya di SMK DIGITAL BREBES, berdasarkan kriteria-kriteria yang telah ditentukan. Sistem Penunjang Keputusan ini menggunakan metode atau algoritma <b>Simple Additive Weighting (SAW)</b>. Metode SAW berfungsi untuk melakukan proses perangkingan dalam menentukan siswa yang terpilih serta memperoleh predikat siswa berprestasi.
            </p>

            <p>
                Simple Additive Weighting (SAW) merupakan salah satu metode pengambilan keputusan multi kriteria yang sederhana dan klasik. Metode ini termasuk dalam metode pembobotan atau dikenal sebagai metode penjumlahan terbobot. Konsep dasar metode SAW adalah mencari penjumlahan terbobot dari rating kinerja pada setiap alternatif terhadap seluruh kriteria.
            </p>

            <p>
                Metode SAW membutuhkan proses normalisasi matriks keputusan (X) ke dalam suatu skala yang dapat diperbandingkan. Metode SAW mengenal dua jenis atribut, yaitu kriteria keuntungan <b>(Benefit)</b> dan kriteria biaya <b>(Cost)</b>. Jika menggunakan atribut Benefit maka akan mencari nilai tertinggi (MAX), sedangkan atribut Cost mencari nilai terendah (MIN).
            </p>

            <h5 class="mt-4 mb-3">Tentang Human Resource Management System (HRMS)</h5>

            <p>
                <b>Human Resource Management System (HRMS)</b> merupakan sistem informasi terintegrasi yang digunakan untuk mengelola sumber daya manusia dalam suatu organisasi, mulai dari pendataan individu, pengelolaan aktivitas, hingga proses penilaian kinerja sebagai dasar dalam pengambilan keputusan.
            </p>

            <p>
                Dalam lingkungan sekolah, konsep HRMS dapat diadaptasi dengan memandang siswa sebagai sumber daya utama yang perlu dikelola dan dievaluasi prestasinya secara sistematis. Oleh karena itu, HRMS dalam sistem ini berperan sebagai sistem manajemen data siswa yang mencakup data identitas siswa, data kelas, data nilai akademik, data kehadiran, data sikap, serta data prestasi non-akademik.
            </p>

            <p>
                Integrasi HRMS dengan Sistem Pendukung Keputusan bertujuan untuk memastikan bahwa proses pengambilan keputusan dilakukan berdasarkan data yang akurat, lengkap, dan terstruktur. HRMS berfungsi sebagai penyedia data, sedangkan SPK dengan metode SAW berfungsi sebagai pengolah data untuk menghasilkan peringkat siswa berprestasi secara objektif.
            </p>

