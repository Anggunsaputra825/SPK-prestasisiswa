    <div style="background:#ffffff; padding:35px; border:1px solid #e3e6f0; border-radius:6px;">

        <h5 class="mb-3">Langkah-langkah penggunaan :</h5>

        <div class="text-justify font-weight-light pr-4">

            <ol>

                <li class="mb-2">
                    <b>Pertama</b>, menambahkan <b>Data Kriteria</b> dengan memasukan (input) nama kriterianya, kemudian masukkan nilai bobot (untuk nilai bobot dapat ditentukan berdasarkan seberapa kepentingannya) adapun cara memasukan nilai yaitu :
                    <ul type="disc">
                        <li>Jika nilai bobot 100% maka yang diinput 1.0 dari 100%</li>
                        <li>Jika nilai bobot 75% maka yang diinput 0.75 dari 75%</li>
                        <li>Jika nilai bobot 30% maka yang diinput 0.3 dari 30%</li>
                    </ul>
                    lalu pilih atribut (Benefit/Cost), Benefit yaitu mencari nilai tertinggi (Max), sedangkan Cost mencari nilai terendah (Min), setelah diinputkan klik <b>Simpan</b>. Untuk memasukan <b>Data Kriteria</b> ada pada bagian pilihan <b>Menu Data > Data Kriteria</b>.
                </li>

                <li class="mb-2">
                    <b>Kedua</b>, menambahkan <b>Data Alternatif</b> yaitu data-data siswa yang menjadi pilihan / alternatif data dalam proses penentuan keputusan, untuk memasukan <b>Data Alternatif</b> ada pada bagian pilihan <b>Menu Data > Data Alternatif</b>.
                </li>

                <li class="mb-2">
                    <b>Ketiga</b>, menambahkan <b>Sub Kriteria</b>, setelah menginput data nama kriteria, dari setiap masing-masing kriteria menambahkan sub kriteria dengan mengklik "Masukan Sub Kriteria". Sub kriteria ini bertujuan sebagai pilihan dalam penilaian awal nantinya, untuk memasukan subkriteria ini ada pada bagian pilihan <b>Menu Data > Data Alternatif</b>.
                </li>

                <li class="mb-2">
                    <b>Keempat</b>, menambahkan data <b>Penilaian Awal</b> pada masing-masing data nama alternatif untuk menambahkan penilaian dengan mengklik button <b>Masukan Penilaian</b>. Terdapat fitur jika ingin melihat penilaian yang telah dimasukan, dengan mengklik button ikon mata (<b>eye</b>) yaitu <b>Lihat Penilaian</b>. Untuk memasukan penilaian ini ada pada bagian pilihan <b>Penilaian Awal</b>.
                </li>

                <li class="mb-2">
                    <b>Kelima</b>, setelah menambahkan penilaian, kemudian melakukan <b>Proses Perhitungan</b> dengan mengklik button <b>Hitung</b>. Untuk melakukan proses ini ada pada bagian pilihan <b>Proses Perhitungan</b>.
                </li>

                <li class="mb-2">
                    <b>Keenam</b>, setelah di-generate maka akan muncul hasilnya pada bagian <b>Hasil Perhitungan</b>. Terdapat 3 paginasi yaitu <b>Hasil Normalisasi</b>, <b>Hasil Preferensi</b>, dan <b>Perankingan</b>. Jika ingin menghapus hasil perhitungan, klik button <b>Reset Hasil</b>.
                </li>

                <li class="mb-2">
                    <b>Ketujuh</b>, jika ingin mencetak laporan dalam bentuk PDF, pilih menu <b>Laporan</b> → <b>Cetak Normalisasi</b> atau <b>Cetak Perankingan</b>, lalu klik <b>Cetak Laporan</b>.
                </li>

                <li>
                    <b>*Note</b> : Jika terdapat perubahan data (tambah, ubah, hapus), cukup klik kembali button <b>Hitung</b> untuk memperbarui hasil perhitungan.
                </li>

            </ol>

        </div>

    </div>

</div>
